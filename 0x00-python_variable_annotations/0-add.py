#!/usr/bin/python3
"""
This script contains a simple function to add two numbers.

Function Documentation:
The script includes a function `add` that takes two
floating-point numbers and returns their sum.
"""


def add(a: float, b: float) -> float:
    """
    Adds two numbers and returns the result.

    Parameters:
    a (float): The first number to add.
    b (float): The second number to add.

    Returns:
    float: The sum of the two numbers.

    Example:
    >>> add(1.2, 2.3)
    3.5
    """
    return a + b
